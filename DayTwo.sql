SELECT SUM(HorizontalPosition) * SUM(Depth) AS GrandTotal
FROM (
	SELECT HorizontalPosition
		, (CASE
			WHEN HorizontalPosition > 0
			THEN HorizontalPosition * CumulativeAim
			ELSE 0 END) AS Depth
	FROM (
		SELECT Id
			, HorizontalPosition
			, SUM(Aim) OVER (ORDER BY Id) AS CumulativeAim
		FROM (
			SELECT Id
				, (CASE
				WHEN Course LIKE 'down%'
				THEN RIGHT(Course,1)
				WHEN Course LIKE 'up%'
				THEN RIGHT(Course,1)*(-1)
				ELSE 0
				END ) AS Aim
				, (CASE
				WHEN Course LIKE 'forward%'
				THEN RIGHT(Course,1)
				ELSE 0
				END ) AS HorizontalPosition
			FROM #AdventOfCodeDayTwo
		) a
	) b
) c