FROM (
	SELECT (CASE 
		WHEN Course LIKE 'down%' 
		THEN RIGHT(Course,1)
		WHEN Course LIKE 'up%' 
		THEN RIGHT(Course,1)*(-1)
		ELSE 0
		END ) AS Depth 
		, (CASE 
		WHEN Course LIKE 'forward%' 
		THEN RIGHT(Course,1)
		ELSE 0
		END ) AS HorizontalPosition
	FROM #AdventOfCodeDayTwo
) a

